package ciao.code.spring.example.demo02springaop.annotation;

import java.lang.annotation.*;

/**
 * @author lengleng
 * @date 2018/6/28
 * 操作日志注解
 */
@Target(ElementType.METHOD)
@Retention(RetentionPolicy.RUNTIME)
@Documented
public @interface SysLog {

	/**
	 * 描述
	 *
	 * @return {String}
	 */
	String value();
}
